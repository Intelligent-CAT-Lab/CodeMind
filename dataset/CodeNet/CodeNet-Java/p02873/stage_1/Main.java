        while(ptr < n){
var newVariable_0 = str.charAt(ptr);            if(newVariable_0 == '>'){
var newVariable_1 = str.charAt(ptr);                while(ptr < n && newVariable_1 == '>'){
                    ptr++;
                    cnt++;
                }
                if(curr > cnt){
                    sum+=(curr+(((cnt)*(cnt-1))/2));
                }else{
                    sum+=(((cnt+1)*(cnt))/2);
                }
                curr = 0;
                cnt = 0;
            }else{
                sum+=curr;
                curr++;
                ptr++;
            }
        }
var newVariable_2 = str.charAt(n-1);        if(newVariable_2 == '<') sum+=curr;
        out.println(sum);
        out.close();
