import sys

MOD = 1000000007

def pow(l, i):
    if i == 0:
        return 1
    else:
        if i % 2 == 0:
            val = pow(l, i / 2)
            return val * val % 2019
        else:
            return pow(l, i - 1) * l % 2019

def main():
    n, k = map(int, sys.stdin.readline().split())
    ans = 0
    for i in range(k, n + 2):
        f = i * (i - 1) / 2
        l = (n * 2 - i + 1) * i / 2
        add = l - f + 1
        ans = (ans + add) % MOD
    print(ans)

if __name__ == '__main__':
    main()