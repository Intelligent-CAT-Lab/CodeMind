import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner stdin = new Scanner(System.in);
        String s = stdin.nextLine();
        String result = "Yes";
        if (s.contains("A") && s.contains("B")) {
            result = "Yes";
        } else {
            result = "No";
        }
        System.out.println(result);
    }
}
