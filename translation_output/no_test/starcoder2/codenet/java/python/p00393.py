dp[0][0] = 1
for i in range(1, n+1):
    dp[i][0] += dp[i-1][0] + dp[i-1][1]
    dp[i][0] %= mod
    dp[i][1] += dp[i-1][0] + dp[i-1][1]
    dp[i][1] %= mod
    if i-m >= 0:
        dp[i][1] = (dp[i][1] - dp[i-m][0] + mod) % mod

print((pow(2, n, mod) - (dp[n][0] + dp[n][1]) + mod * mod) % mod)
