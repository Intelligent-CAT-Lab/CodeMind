import java.util.*;
public class codeforces_651_A {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a1, a2, t = 0;
        a1 = sc.nextInt();
        a2 = sc.nextInt();
        if (a1 == 1 && a2 == 1) {
            System.out.println(0);
        } else {
            while (a1 > 0 && a2 > 0) {
                if (a1 > a2) {
                    a2 += 1;
                    a1 -= 2;
                } else {
                    a2 -= 2;
                    a1 += 1;
                }
                t += 1;
            }
            System.out.println(t);
        }
    }
}