import java.util.*;

public class p01610 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.nextLine();
        String[] s = {"..rr....", "..rr....","ggyybbww","ggyybbww","..oo....","..oo...."};
        for(int i = 0; i < a.length(); i++) {
            s = rotate(s, a.charAt(i));
        }
        for(String row : s) {
            System.out.println(row);
        }
    }

    public static String[] rotate(String[] s, char k) {
        String[][] s_ = new String[6][8];
        for(int i = 0; i < 6; i++) {
            for(int j = 0; j < 8; j++) {
                s_[i][j] = s[i].substring(j, j+1);
            }
        }
        if(k == 'R') {
            s_[0][3] = s_[2][3];
            s_[1][3] = s_[3][3];
            s_[2][3] = s_[4][3];
            s_[3][3] = s_[5][3];

            s_[4][3] = s_[3][6];
            s_[5][3] = s_[2][6];
            s_[3][6] = s_[0][3];
            s_[2][6] = s_[1][3];

            s_[2][4] = s_[3][4];
            s_[3][4] = s_[3][5];
            s_[3][5] = s_[2][5];
            s_[2][5] = s_[2][4];
        } else if(k == 'L') {
            s_[0][2] = s_[3][7];
            s_[1][2] = s_[2][7];
            s_[2][2] = s_[0][2];
            s_[3][2] = s_[1][2];

            s_[4][2] = s_[2][2];
            s_[5][2] = s_[3][2];
            s_[3][7] = s_[4][2];
            s_[2][7] = s_[5][2];

            s_[2][1] = s_[2][0];
            s_[2][0] = s_[3][0];
            s_[3][0] = s_[3][1];
            s_[3][1] = s_[2][1];
        } else if(k == 'U') {
            s_[2][0] = s_[2][2];
            s_[2][1] = s_[2][3];
            s_[2][2] = s_[2][4];
            s_[2][3] = s_[2][5];

            s_[2][4] = s_[2][6];
            s_[2][5] = s_[2][7];
            s_[2][6] = s_[2][0];
            s_[2][7] = s_[2][1];

            s_[0][2] = s_[1][2];
            s_[1][2] = s_[1][3];
            s_[1][3] = s_[0][3];
            s_[0][3] = s_[0][2];
        } else if(k == 'D') {
            s_[3][0] = s_[3][6];
            s_[3][1] = s_[3][7];
            s_[3][2] = s_[3][0];
            s_[3][3] = s_[3][1];

            s_[3][4] = s_[3][2];
            s_[3][5] = s_[3][3];
            s_[3][6] = s_[3][4];
            s_[3][7] = s_[3][5];

            s_[4][2] = s_[5][2];
            s_[5][2] = s_[5][3];
            s_[5][3] = s_[4][3];
            s_[4][3] = s_[4][2];
        } else if(k == 'F') {
            s_[1][2] = s_[3][1];
            s_[1][3] = s_[2][1];
            s_[3][1] = s_[4][3];
            s_[2][1] = s_[4][2];

            s_[4][3] = s_[2][4];
            s_[4][2] = s_[3][4];
            s_[2][4] = s_[1][2];
            s_[3][4] = s_[1][3];

            s_[2][2] = s_[3][2];
            s_[3][2] = s_[3][3];
            s_[3][3] = s_[2][3];
            s_[2][3] = s_[2][2];
        } else {
            s_[0][2] = s_[2][5];
            s_[0][3] = s_[3][5];
            s_[2][5] = s_[5][3];
            s_[3][5] = s_[5][2];

            s_[5][3] = s_[3][0];
            s_[5][2] = s_[2][0];
            s_[3][0] = s_[0][2];
            s_[2][0] = s_[0][3];

            s_[2][6] = s_[3][6];
            s_[3][6] = s_[3][7];
            s_[3][7] = s_[2][7];
            s_[2][7] = s_[2][6];
        }
        for(int i = 0; i < 6; i++) {
            for(int j = 0; j < 8; j++) {
                s[i] = s[i].substring(0, j) + s_[i][j] + s[i].substring(j+1);
            }
        }
        return s;
    }
}